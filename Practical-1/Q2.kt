fun main(){
    //Integer To Double
    var value1 = 100
    val value2: Double = value1.toDouble()
    println(value2);

    //String To Integer
    var value3 = "100"
    val value4: Int = value3.toInt()
    println(value4);

    //String To Double
    var value5 = "100"
    val value6: Double = value5.toDouble()
    println(value6);
}